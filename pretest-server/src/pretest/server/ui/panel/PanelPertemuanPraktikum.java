    private void statusAwal() {
        reModelComboPraktikum();
        clearText();
        loadAllData();
        isiTable();
        buttonHapus.setEnabled(false);
        buttonUbah.setEnabled(false);
        buttonSimpan.setEnabled(true);
    }

    private void isiTable() {
        Object data[][] = new Object[pertemuanPraktikums.size()][2];
        int row = 0;
        for (PertemuanPraktikum pp : pertemuanPraktikums) {
            data[row][0] = pp.getPraktikum().getNama();
            data[row][1] = pp.getPertemuanKe();

            row++;
        }
        String titile[] = {"Praktikum", "Pertemuan Ke"};
        tablePertemuanPraktikum.setModel(new DefaultTableModel(data, titile));
        jScrollPane1.setViewportView(tablePertemuanPraktikum);
    }

    private void loadAllData() {
       
            pertemuanPraktikums = new ArrayList<PertemuanPraktikum>();
            pertemuanPraktikums = praktikumService.findPertemuanPraktikums();
        
    }

