    private void clearText() {
        textKe.setText("");
        comboPraktikum.setSelectedIndex(-1);
    }

    private void isiText() {
        textKe.setText(pertemuanPraktikum.getPertemuanKe() + "");
        comboPraktikum.setSelectedIndex(praktikums.indexOf(pertemuanPraktikum.getPraktikum()));
    }

    private void initSelectTable() {
        if (tablePertemuanPraktikum.getSelectedRow() >= 0) {
            pertemuanPraktikum = pertemuanPraktikums.get(tablePertemuanPraktikum.getSelectedRow());
            isiText();
            buttonHapus.setEnabled(true);
            buttonUbah.setEnabled(true);
            buttonSimpan.setEnabled(false);
        }
    }

    private boolean cekText() {
        if (!textKe.getText().isEmpty() && comboPraktikum.getSelectedIndex() >= 0) {
            return true;
        }
        return false;
    }

    private void comboPraktikumItemStateChanged() {
        if (comboPraktikum.getSelectedIndex() >= 0) {
            praktikum = praktikums.get(comboPraktikum.getSelectedIndex());
        }
    }

    private void buttonSimpanActionPerformed() {
        if (cekText()) {
            
                pertemuanPraktikum = new PertemuanPraktikum();
                pertemuanPraktikum.setPertemuanKe(Integer.parseInt(textKe.getText().trim()));
                pertemuanPraktikum.setPraktikum(praktikum);
                praktikumService.save(pertemuanPraktikum);
                statusAwal();
            
        } else {
            JOptionPane.showMessageDialog(null, "text field tidak boleh kosong");
        }
    }

    private void buttonUbahActionPerformed() {
        if (cekText()) {
            
                pertemuanPraktikum.setPertemuanKe(Integer.parseInt(textKe.getText().trim()));
                pertemuanPraktikum.setPraktikum(praktikum);
                praktikumService.save(praktikum);
                statusAwal();
            
        } else {
            JOptionPane.showMessageDialog(null, "text field tidak boleh kosong");
        }
    }

    private void buttonHapusActionPerformed() {
        if (tablePertemuanPraktikum.getSelectedRow() >= 0) {
           
                pertemuanPraktikum = pertemuanPraktikums.get(tablePertemuanPraktikum.getSelectedRow());
                praktikumService.delete(pertemuanPraktikum);
                JOptionPane.showMessageDialog(null, "data sudah dihapus");
            
        }
        statusAwal();
    }

    public void reModelComboPraktikum() {
        if (!comboPraktikum.isVisible()) {
            return;
        }

        boolean[] fieldVisible = {false, true};
        praktikums=praktikumService.findPraktikums();
        BeanComboBoxModel model = new BeanComboBoxModel(praktikums, fieldVisible, " - ");
        comboPraktikum.setModel(model);
//        country = null;
    }
  
